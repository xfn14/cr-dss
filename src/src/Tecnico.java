public class Tecnico {
    private String idTecnico;

    public Tecnico(){
        this.idTecnico = "";
    }

    public Tecnico(String idTecnico) {
        this.idTecnico = idTecnico;
    }

    public String getIdTecnico() {
        return idTecnico;
    }

    public void setIdTecnico(String idTecnico) {
        this.idTecnico = idTecnico;
    }
}
